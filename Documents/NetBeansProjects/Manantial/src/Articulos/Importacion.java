public class Importacion {
    private int numneroColumna;
    private int tipoColumna;
    private String descripcion;

    public int getNumneroColumna() {
        return numneroColumna;
    }

    public void setNumneroColumna(int numneroColumna) {
        this.numneroColumna = numneroColumna;
    }

    public int getTipoColumna() {
        return tipoColumna;
    }

    public void setTipoColumna(int tipoColumna) {
        this.tipoColumna = tipoColumna;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    
}
