package Impresiones;

/**
 *
 * @author Usuario
 */
public class FormatoPie {
    
    private String saldoInicial;
    private String totalVentas;
    private String totlaGastos;
    private String retiros;
    private String pagos;
    private String saldo;
    
    public String getSaldoInicial() {
        return saldoInicial;
    }

    public void setSaldoInicial(String saldoInicial) {
        this.saldoInicial = saldoInicial;
    }

    public String getTotalVentas() {
        return totalVentas;
    }

    public void setTotalVentas(String totalVentas) {
        this.totalVentas = totalVentas;
    }

    public String getTotlaGastos() {
        return totlaGastos;
    }

    public void setTotlaGastos(String totlaGastos) {
        this.totlaGastos = totlaGastos;
    }

    public String getRetiros() {
        return retiros;
    }

    public void setRetiros(String retiros) {
        this.retiros = retiros;
    }

    public String getPagos() {
        return pagos;
    }

    public void setPagos(String pagos) {
        this.pagos = pagos;
    }

    public String getSaldo() {
        return saldo;
    }

    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }
    
    
}
