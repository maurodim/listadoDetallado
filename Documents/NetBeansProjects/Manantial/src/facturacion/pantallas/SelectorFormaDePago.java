    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int posicion=this.jComboBox1.getSelectedIndex();
        formaP=(FormasDePago) listadoF.get(posicion);
        double mmtt=Double.parseDouble(this.monto_txt.getText());
        mmtt=mmtt * formaP.getRecargo();
        if(mmtt > saldo)saldo=mmtt;
        mmtt=Numeros.Redondear(mmtt);
        this.monto_txt.setText(String.valueOf(mmtt));
        this.monto_txt.selectAll();
        this.jButton2.requestFocus();
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        formaP.setMonto(Numeros.ConvertirStringADouble(this.monto_txt.getText()));
        listadoPagos.add(formaP);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

